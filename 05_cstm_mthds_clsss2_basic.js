// # 1. Write a Song class with attributes for name, artist, and duration.
class Song {
  constructor(name, artist, duration) {
    this.name = name;
    this.artist = artist;
    this.duration = duration;
  }
}

let song = new Song("Dancing Queen", "Abba", "3:50");
console.log(song);

